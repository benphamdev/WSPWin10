package TTTA;

public class LopHoc {
	private String malh;
	private String tenlop;
	public LopHoc(String malh, String tenlop) {
		super();
		this.malh = malh;
		this.tenlop = tenlop;
	}
	public String getMalh() {
		return malh;
	}
	public void setMalh(String malh) {
		this.malh = malh;
	}
	public String getTenlop() {
		return tenlop;
	}
	public void setTenlop(String tenlop) {
		this.tenlop = tenlop;
	}
	
}
